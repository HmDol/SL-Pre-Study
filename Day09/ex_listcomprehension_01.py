# list comprehension : [] + for in + if else 
# -----------------------------------------------------------------

## 요청. data1 리스트이 요소에 3을 곱한 값을 새로운 리스트에 저장
data1 = [ 1, 2, 3]

## 방법1
data2 = []
for d in data1 :
    data2.append(d*3)

##방법2
res = [num*3 for num in data1]
#      --2--  -------1------

##결과 출력
print(res)      # 같은 결과
print(data2)    # 같은 결과
