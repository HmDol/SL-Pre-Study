## 요청 . 3단 출력

idx = 1 
while idx < 10 :
    print(f'3 * {idx} = {3*idx}')
    idx+=1

print()
## 요청. 구구단 출력

idx2 = 2
while idx2 < 10 :
    print(f'{idx2}단')
    idx1 = 1
    while idx1 < 10 :
        print(f'{idx2} * {idx1} = {idx1 * idx2}')
        idx1+=1
    idx2 += 1    
    print()    