# 변수 선언 및 사용법
# -> Python에서는 모든 변수가 데이터의 주소를 저장함!
# -----------------------------------------
# 선언 형식 : 변수명 = 데이터

# 숫자데이터 -> 변수명 = 데이터
age = 10
avg = 99.98888
score = .9 # 0.9 -> 실수
score = 8. # 8.0 -> 실수

# 글자 데이터 => 변수명 = '데이터' 또는 변수명 = "데이터" 
#               또는 변수명 = '''데이터''' 또는 변수명 = """데이터"""
name = '홍길동'
name = "김홍도"
name = '''태권V'''
msg = '''오늘은  
         좋은날
         내일도
         좋은날 ''' # 여러줄이 한덩어리로 저장됨
